    /**
     * Metodo publico que nod evuelve nada y que muestra en una segunda ventana
     * los atributos del libro seleccionado
     *
     * @param bk objeto de la clase Book
     */
    public void showBook(Book bk) {
        lTitleSW.setText(bk.getTitle());
        lAutorSW.setText(bk.getAuthor());
        lEditorialSW.setText(bk.getEditorial());
        lAgeSW.setText(bk.getAge());
        lLiteraryStyleSW.setText(bk.getSelectedGenreAsString());
    }

    /**
     * Metodo privado que no devuelve nada, ajusta la ventana al centro y el
     * tama√±o respectivo de la resolucion del monitor
     */
    private void responsiveDesign() {
        Dimension pantalla = Toolkit.getDefaultToolkit().getScreenSize();
        int height = pantalla.height;
        int width = pantalla.width;
        setSize(width / 4, height / 4);
        setLocationRelativeTo(null);
        setVisible(true);
    }

